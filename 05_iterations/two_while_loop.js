// while loop


let i = 0
while (i <= 10) {
    console.log(`Value of i is ${i}`);
    i = i + 2
    
}

let myArray = ["flash", "batman","superman"]

let arr = 0
while (arr < myArray.length) {
    console.log(`Value is ${myArray[arr]}`);
    arr = arr + 1
}


// do while loop - in do while loop, code first get executed and then condition is checked through while loop 
let score = 12
do {
    console.log(`Score is ${score}`)             
    score++
} while (score <= 10);
